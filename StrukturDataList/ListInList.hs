x = [[1, 2, 3, 4], [5, 3, 3, 3], [1, 2, 2, 3, 4], [1, 2, 3]]

main = do
  print x
  -- [[1,2,3,4],[5,3,3,3],[1,2,2,3,4],[1,2,3]]

  print (x ++ [[1, 1, 1, 1]])
  -- [[1,2,3,4],[5,3,3,3],[1,2,2,3,4],[1,2,3],[1,1,1,1]]

  print ([6, 6, 6] : x)
  -- [[6,6,6],[1,2,3,4],[5,3,3,3],[1,2,2,3,4],[1,2,3]]

  print (x !! 2)
  -- [1,2,2,3,4]

  print '\0'